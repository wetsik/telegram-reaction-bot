def _join(a: str, b: str, c: str, d: str) -> str:
    parts = [a, b, c, d]
    return " ".join(part for part in parts if part).replace("  ", " ").strip()


def build_smart_database() -> dict[str, list[str]]:
    explanation = [
        _join(a, b, c, d)
        for a, b, c, d in product(OPENERS, SUBJECTS, VERBS, QUALIFIERS)
    ]
    deep_explanation = [
        f"{a}, {b}. {c}"
        for a, b, c in product(OPENERS, DEEP_CUES, EXPLANATORY_ENDINGS)
    ]
    practical = [
        f"{a} {b}, {c}"
        for a, b, c in product(OPENERS, PRACTICAL_CUES, EXPLANATORY_ENDINGS)
    ]
    social = [
        f"{a} {b}"
        for a, b in product(SOCIAL_CUES, EXPLANATORY_ENDINGS)
    ]
    analogy = [
        f"{a} {b}, {c}"
        for a, b, c in product(OPENERS, ANALOGIES, EXPLANATORY_ENDINGS)
    ]
    reflection = [
        f"{a}. {b}"
        for a, b in product(REFLECTIONS, EXPLANATORY_ENDINGS)
    ]
    neutral = SHORT_REACTIONS + social + reflection[:40]

    return {
        "explanation": explanation,
        "deep": deep_explanation + reflection + analogy,
        "practical": practical,
        "social": social,
        "neutral": neutral,
    }


SMART_DATABASE = build_smart_database()
